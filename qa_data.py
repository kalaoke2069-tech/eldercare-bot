def find_answer(user_message):
    """根據用戶訊息找到最合適的 Q&A 回覆"""
    user_lower = user_message.lower().strip()
    
    # 嘗試精確匹配關鍵字
    best_match = None
    best_score = 0
    
    for qa in QA_PAIRS:
        score = 0
        for keyword in qa["keywords"]:
            if keyword in user_lower:
                score += 1
        
        if score > best_score:
            best_score = score
            best_match = qa
    
    # 如果找到匹配，回覆答案
    if best_match and best_score > 0:
        return best_match["answer"]
    
    return None
